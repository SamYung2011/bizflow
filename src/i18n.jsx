const I18nContext = createContext({ lang: "zh", setLang: () => {}, t: (s) => s });

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(() => {
    try { return localStorage.getItem(STORAGE_KEY) || "zh"; } catch { return "zh"; }
  });
  useEffect(() => {
    try { localStorage.setItem(STORAGE_KEY, lang); } catch {}
  }, [lang]);
  const setLang = (l) => setLangState(l === "en" ? "en" : "zh");
  const t = (zh, params) => {
    let out;
    if (lang === "en") out = DICT_EN[zh] ?? zh;
    else out = zh;
    if (params && typeof out === "string") {
      for (const k in params) out = out.replace(new RegExp(`\\{${k}\\}`, "g"), params[k]);
    }
    return out;
  };
  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

export function useT() {
  return useContext(I18nContext);
}

// 给字典加翻译用的辅助：在外部按需调用，比如 register('客戶名', 'Customer Name')
export function register(zh, en) {
  DICT_EN[zh] = en;
}
